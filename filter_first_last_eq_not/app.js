//_____________________filter,first,last,eq,not_______________________________


$(document).ready(function(){


        $("div").first().css("background","#999");

        $("div").last().css("background","#899");

        $("div").filter(".div2").css("background","#444");

        $("div").eq(0).css("background","#e25a");

        $("div").not(".div2").css("background","#f29");












































})